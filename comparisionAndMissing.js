async function fetchApiData(url) {
    try {
        const response = await fetch(url);
        if (!response.ok) throw new Error(`HTTP error! Status: ${response.status}`);
        return await response.json();
    } catch (error) {
        console.error("Error fetching API data:", error);
        return null;
    }
}

function compareJSON(apiData, realData, prefix = '') {
    let differences = { missingKeys: [], differentValues: [] };

    for (let key in realData) {

        if (!(key in apiData)) {
            differences.missingKeys.push(key);
        } else if (typeof realData[key] === 'object' && realData[key] !== null) {
            let subDifferences = compareJSON(apiData[key], realData[key], key);
            differences.missingKeys.push(...subDifferences.missingKeys);
            differences.differentValues.push(...subDifferences.differentValues);
        } else if (apiData[key] !== realData[key]) {
            differences.differentValues.push({ key: key, apiValue: apiData[key], realValue: realData[key] });
        }
    }

    return differences;
}

